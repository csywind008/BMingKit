/**
 获取系统语言
 */
+ (NSString *)getPreferredLanguage{
    static dispatch_once_t onceToken;
    static NSString *preferredLang = nil;
    dispatch_once(&onceToken, ^{
        NSArray *languages = [[NSUserDefaults standardUserDefaults] objectForKey:@"AppleLanguages"];
        preferredLang = [languages firstObject];
    });
    
    return preferredLang;
}

/**
 判断是否中文
 */
+ (BOOL)isChineseLanguage {
    static dispatch_once_t onceToken;
    static BOOL temp = NO;
    dispatch_once(&onceToken, ^{
        
        if ([[self getPreferredLanguage] rangeOfString:@"zh"].location != NSNotFound) {
            temp = YES;
        } else {
            temp = NO;
        }
        
    });
    return temp;
}

/**
 获取APP版本号
 默认显示2个小数点的模式,例如3.0改为3.0.0
 */
+ (NSString *)getAPPVersion {
    static dispatch_once_t onceToken;
    static NSDictionary *infoDictionary = nil;
    static NSString *version = nil;
    dispatch_once(&onceToken, ^{
        infoDictionary = [[NSBundle mainBundle] infoDictionary];
        version = [infoDictionary objectForKey:@"CFBundleShortVersionString"];
        // 默认显示2个小数点的模式,例如3.0改为3.0.0
        if ([version componentsSeparatedByString:@"."].count - 1 == 1) {
            version = [version stringByAppendingString:@".0"];
        }
    });
    return version;
}
